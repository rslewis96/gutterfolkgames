type submittedCard = {
    SubmitterSocketID: string
    SubmitterScreenName: string
    CardText: string
    WinningCard: boolean
}

type LoginInfo = {
    ScreenName: string
    SentFrom: string
    Password: string
    Mascot: string
}

type ChatMessage = {
    ID: number
    Type: "GameMessage" | "PlayerMessage"
    SenderScreenName: string
    SenderChatHandle: string
    Text: string
}


